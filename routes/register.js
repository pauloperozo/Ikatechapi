//////////////////////////////////////////////////////////////////////////////////////////
const { Router } = require('express')
const router = Router()
//////////////////////////////////////////////////////////////////////////////////////////
const RegisterValidations = require('../validations/register.js')
//////////////////////////////////////////////////////////////////////////////////////////
router.post('/',RegisterValidations,async (req, res) => {

    const { login,password,idProfile } = req.body
    const { Create } = require('../controllers/user')
    const bcryptjs   = require('bcryptjs')
    const resultado  = await Create({login,password: bcryptjs.hashSync(password,10),idProfile})
    res.status( resultado.status ).send( resultado.respuesta )

})
//////////////////////////////////////////////////////////////////////////////////////////
module.exports = router
